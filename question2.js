//using if-else 
let x = 50;
let y = 1000;
let max;

if (x > y) {
  max = x;
} else {
  max = y;
}

console.log("The larger value is: " + max); // Output: The larger value is: 1000

//using ternary operators 
let x1 = 6;
let y1 = 10;

let max1 = (x1 > y1) ? x1 : y1;

console.log("The larger value is: " + max1); // Output: The larger value is: 10

